    class Solution {
        public int[] twoSum(int[] nums, int target) {
            Map<Integer, Integer> map = new HashMap<>();
            for (int i = 0; i < nums.length; i++) {
                Integer value = map.get(target - nums[i]);
                if (value == null) {
                    map.put(nums[i], i);
                } else {
                    return new int[]{i, value};
                }
            }
            return new int[0];
        }
    }

